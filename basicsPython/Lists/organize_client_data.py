# Список имен клиентов
first_names = ["Анна", "Борис", "Александр", "Денис"]

# Создаем пустой список для возрастов
age = []

# Возраст Дениса
age.append(42)

# Создаем список возрастов для всех клиентов
ages_of_others = [32, 41, 29]  # Возраст Анны, Бориса и Александра
all_ages = ages_of_others + age  # Объединяем с возрастом Дениса

# Объединяем имена и возраста с помощью zip
name_and_age = list(zip(first_names, all_ages))

# Создаем диапазон идентификаторов
ids = range(4)  # Идентификаторы от 0 до 3

print("Имя и возраст:", name_and_age)
print("Идентификаторы клиентов:", list(ids))
