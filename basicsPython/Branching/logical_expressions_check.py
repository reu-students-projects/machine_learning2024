# Проверяем истинность выражений and
statement_1 = (2 + 2 + 2 >= 6) and (-1 * -1 < 0)
statement_2 = (4 * 2 <= 8) and (7 - 1 == 6)

# Проверяем истинность выражений or
statement_3 = (2 - 1 > 3) or (-5 * 2 == -10)
statement_4 = (9 + 5 <= 15) or (7 != 4 + 3)

print("Утверждение первое:", statement_1)
print("Утверждение второе:", statement_2)
print("Утверждение третье:", statement_3)
print("Утверждение четвертое:", statement_4)
